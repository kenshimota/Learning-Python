# cycles For este lo puede usar
# como un each pero solo para listas y tuplas

my_values = {
	'valor 1'
	'valor 2'
	'valor 3'
}

# usando for como un each
for values in my_values:
	print(values)


# usando for como un rango de valores
# i sera igual a cero, mientras i sea menor de cuatro
# repite esto, al finalizar cada ciclo i incrementa
for i in range(0,4):
	print(i)
